void	check_death(t_val *val)
{
	double	dx;
	double	dy;

	dx = val->game->enemy_x - val->game->plyr_x;
	dy = val->game->enemy_y - val->game->plyr_y;
	dx = fabs(dx);
	dy = fabs(dy);
	if (dx < TILE / 2 && dy < TILE / 2)
	{
		draw_death1();
		exit(0);
	}
}
